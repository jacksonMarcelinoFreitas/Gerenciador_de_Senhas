    Conexao con=new Conexao();
    
    public void cadastrarLogin(){
        try {
            Connection c=con.conectar();

            PreparedStatement cadastrarStmt=c.prepareStatement("insert into Login(url,tipo,fk_idUsuario,fk_idSenha) values (?,?,?,?)");

            cadastrarStmt.setString(1, txtUrl.getText());      
            cadastrarStmt.setString(1, labelTipo.getText());      
            cadastrarStmt.setString(1, txtIdUsuario.getText());      
            cadastrarStmt.setString(1, txtIdSenha.getText());      
            cadastrarStmt.execute();
            cadastrarStmt.close();
            JOptionPane.showMessageDialog(null, "Login cadastrado com sucesso...");
            con.desconectar();

        } catch (SQLException e) {
            System.err.println("Erro ao cadastrar login!");
            System.err.println(e.getMessage());
        }finally{
            con.desconectar();
        }
    }
    
