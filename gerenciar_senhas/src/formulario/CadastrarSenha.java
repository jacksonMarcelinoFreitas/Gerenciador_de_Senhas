    Conexao con=new Conexao();
    
    public void cadastrarSenha(){
        try {
            Connection c=con.conectar();

            PreparedStatement cadastrarStmt=c.prepareStatement("insert into Senha(senha) values (?)");

            cadastrarStmt.setString(1, txtSenha.getText());      
            cadastrarStmt.execute();
            cadastrarStmt.close();
            JOptionPane.showMessageDialog(null, "Senha cadastrada com sucesso...");
            con.desconectar();

        } catch (SQLException e) {
            System.err.println("Error ao cadastrar senha");
            System.err.println(e.getMessage());
        }finally{
            con.desconectar();
        }
    }
