    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
        try {
            Connection c=con.conectar();
            PreparedStatement pesquisarStmt = c.prepareStatement("SELECT * FROM Usuario where Nome like '%"+ txtNome.getText()+"%'");              
            ResultSet rs = pesquisarStmt.executeQuery();                          
            //Exibindo dados da pesquisa na tabela
            DefaultTableModel model;
            model = (DefaultTableModel) tabelaPesquisar.getModel();
            model.setNumRows(0);
            while(rs.next()){
                model.addRow(
                new Object[]{
                    rs.getString("nome"),
                    rs.getString("sobrenome")
                });                                             
            }    
        } catch (SQLException e) {
            System.out.println("ocorreu um erro ao conectar");
        }                               
    }//GEN-LAST:event_txtNomeKeyReleased

    private void btnSalvarAlteracaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarAlteracaoActionPerformed
        // TODO add your handling code here:
        try {
            Connection c=con.conectar();
            PreparedStatement alterarStmt = c.prepareStatement("UPDATE Usuario SET nome=?, sobrenome=? WHERE nome=?");       
            alterarStmt.setString(1, txtNome.getText());//txtnome  tem de vir primeiro conforme a ordem
            alterarStmt.setString(2, txtSobrenome.getText());
            alterarStmt.execute();
            alterarStmt.close();
            JOptionPane.showMessageDialog(null, "Banco atualizado com sucesso...");
            con.desconectar();         
        } catch (SQLException e) {
            System.err.println("Erro ao guardar os dados ");
            System.err.println(e.getMessage());
        }finally{
            con.desconectar();
        }                                        
    }//GEN-LAST:event_btnSalvarAlteracaoActionPerformed

    private void tabelaPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaPesquisarMouseClicked
        int linha = tabelaPesquisar.getSelectedRow();
        txtNome.setText(tabelaPesquisar.getValueAt(linha, 0).toString());
        txtSobrenome.setText(tabelaPesquisar.getValueAt(linha, 1).toString());
    }//GEN-LAST:event_tabelaPesquisarMouseClicked

 
